using UnityEngine;

public class FPSButtonController : MonoBehaviour
{
    public GameObject fpsMonitor;
    public bool fpsIsShowing;

    void Start()
    {
        fpsIsShowing = false;
    }

    public void FPSButtonClicked()
    {
        if(fpsIsShowing)
        {
            fpsMonitor .set
        }
    }
}
